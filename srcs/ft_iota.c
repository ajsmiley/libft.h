#include "libft.h"

char	*iota(int n)
{
	int sign;
	char *newstr;

	sign = 0;
	if (n == '-' || n == '+')
	{
		if (n == '-')
			sign = -1;
		n++;
	}
	while (n >= '0' && n <= '9')
	{
		newstr = n + '0';
	}
}
